import sortingHealth from '../heroes';

test('Correct sorting test', () => {
  const names = [
    { name: 'мечник', health: 10 },
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
  ];
  const sortedNames = [
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
    { name: 'мечник', health: 10 },
  ];
  const result = sortingHealth(names);
  expect(result).toEqual(sortedNames);
});
